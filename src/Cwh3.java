
public class Cwh3 {
    public static void main(String[] args) {
        System.out.println("This is the half * triangle");
        int n=5;
        for (int i = 1; i <= n; i++) {
            for (int j = 1; j <= i; j++) {
                System.out.print("*");
            }
            System.out.print("\n");
        }
    }
}

